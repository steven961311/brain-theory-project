def digit_templates() -> np.ndarray:
    """Return the ten fixed templates as shape (10, 96), dtype int8."""
    result = np.empty((10, 96), dtype=np.int8)
    for label, rows in enumerate(_DIGITS):
        if len(rows) != 12 or any(len(row) != 8 for row in rows):
            raise ValueError(f"invalid 8x12 template for digit {label}")
        result[label] = np.fromiter(
            (1 if char == "#" else -1 for row in rows for char in row),
            dtype=np.int8,
            count=96,
        )
    return result


def decode_template(bits: np.ndarray) -> int:
    """Decode a 96-value state by maximum correlation, ties favoring low digits."""
    vector = np.asarray(bits, dtype=np.float32).reshape(96)
    scores = digit_templates().astype(np.float32) @ vector
    return int(np.argmax(scores))

